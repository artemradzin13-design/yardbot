LOGISTICS_KEYWORDS = ["доставка", "логистика", "везти", "довезти", "стоимость доставки",
                      "цена доставки", "сколько доставить", "доставить до", "до города",
                      "до посёлка", "привезти", "транспорт до"]

def is_logistics_query(text: str) -> str | None:
    """Возвращает название города если это запрос на логистику."""
    text_lower = text.lower()
    for kw in LOGISTICS_KEYWORDS:
        if kw in text_lower:
            # Пробуем извлечь город — слово после "до" или "в"
            words = text.split()
            for i, word in enumerate(words):
                if word.lower() in ["до", "в", "до города", "доставка"]:
                    if i + 1 < len(words):
                        return words[i + 1].strip("?.,!")
    return None

