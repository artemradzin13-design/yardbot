def haversine(lat1, lon1, lat2, lon2):
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat/2)**2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon/2)**2
    return R * 2 * math.atan2(math.sqrt(a), math.sqrt(1-a))

def get_surcharge(km):
    if km <= 50: return 5000
    if km <= 100: return 10000
    if km <= 150: return 15000
    if km <= 200: return 20000
    return 20000 + math.ceil((km - 200) / 50) * 5000

async def geocode_yandex(city: str):
    import urllib.request, json, urllib.parse
    query = urllib.parse.quote(f"{city}, Россия")
    url = f"https://geocode-maps.yandex.ru/1.x/?apikey={YANDEX_GEO_KEY}&geocode={query}&format=json&results=1"
    try:
        with urllib.request.urlopen(url, timeout=5) as r:
            data = json.loads(r.read())
        pos = data["response"]["GeoObjectCollection"]["featureMember"][0]["GeoObject"]["Point"]["pos"]
        lon, lat = map(float, pos.split())
        return lat, lon
    except Exception:
        return None, None

def calculate_logistics(lat, lon):
    best = None
    best_dist = float("inf")
    for stock, cities in BASE_CITIES.items():
        for city in cities:
            d = haversine(lat, lon, city["lat"], city["lon"])
            if d < best_dist:
                best_dist = d
                best = {"stock": stock, "city": city, "dist": round(d)}
    if not best:
        return None
    stock_lat, stock_lon = STOCK_COORDS[best["stock"]]
    base_lat, base_lon = best["city"]["lat"], best["city"]["lon"]
    closer = haversine(lat, lon, stock_lat, stock_lon) < haversine(base_lat, base_lon, stock_lat, stock_lon)
    surcharge = 0 if closer else get_surcharge(best["dist"])
    total = best["city"]["price"] + surcharge
    return {"stock": best["stock"], "base_city": best["city"]["name"], "base_price": best["city"]["price"],
            "dist": best["dist"], "surcharge": surcharge, "total": total, "closer": closer}

