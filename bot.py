conversation_history = {}

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_id = update.effective_user.id
    text = update.message.text.strip()

    if user_id not in conversation_history:
        conversation_history[user_id] = []

    await update.message.chat.send_action("typing")

    # Проверяем — это запрос логистики?
    city_query = is_logistics_query(text)
    logistics_result = None

    if city_query:
        lat, lon = await geocode_yandex(city_query)
        if lat and lon:
            logistics_result = calculate_logistics(lat, lon)

    # Формируем сообщение для Claude
    user_message = text
    if logistics_result:
        user_message += f"\n\n[СИСТЕМА: Автоматический расчёт логистики для '{city_query}':\n"
        user_message += f"Сток: {logistics_result['stock']}\n"
        user_message += f"Базовый город: {logistics_result['base_city']} — {logistics_result['base_price']:,}₽\n"
        if logistics_result['closer']:
            user_message += f"Город ближе к стоку → надбавки нет\n"
        else:
            user_message += f"Расстояние до базового: ~{logistics_result['dist']} км → надбавка +{logistics_result['surcharge']:,}₽\n"
        user_message += f"ИТОГО: {logistics_result['total']:,}₽]"

    # Добавляем в историю
    conversation_history[user_id].append({"role": "user", "content": user_message})

    # Ограничиваем историю (последние 10 сообщений)
    if len(conversation_history[user_id]) > 10:
        conversation_history[user_id] = conversation_history[user_id][-10:]

    # Запрос к Claude
    try:
        response = client.messages.create(
            model="claude-sonnet-4-20250514",
            max_tokens=1000,
            system=SYSTEM_PROMPT,
            messages=conversation_history[user_id]
        )
        answer = response.content[0].text
        conversation_history[user_id].append({"role": "assistant", "content": answer})
        await update.message.reply_text(answer)
    except Exception as e:
        await update.message.reply_text(f"Ошибка: {str(e)}")



